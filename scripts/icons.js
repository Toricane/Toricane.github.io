const FA_CLASS_MAP = [
  ["fa-linkedin", "linkedin"],
  ["fa-envelope", "envelope"],
  ["fa-github", "github"],
  ["fa-x-twitter", "xTwitter"],
  ["fa-youtube", "youtube"],
  ["fa-instagram", "instagram"],
  ["fa-medium", "medium"],
  ["fa-moon", "moon"],
  ["fa-sun", "sun"],
  ["fa-chevron-down", "chevronDown"],
];

export function iconNameFromClass(className = "") {
  for (const [faClass, name] of FA_CLASS_MAP) {
    if (className.includes(faClass)) return name;
  }
  return null;
}

export function iconHtml(name) {
  return ICONS[name] || "";
}

/** Replace <i class="fa-..."> with inline SVG in a document or element. */
export function replaceFaIcons(root) {
  const doc = root.ownerDocument || root;
  root.querySelectorAll("i[class*='fa-']").forEach((el) => {
    const name = iconNameFromClass(el.className);
    if (!name) return;
    const span = doc.createElement("span");
    span.className = "svg-icon";
    span.innerHTML = iconHtml(name);
    el.replaceWith(span);
  });
}

const HREF_ICON_RULES = [
  ["linkedin.com", "linkedin"],
  ["mailto:", "envelope"],
  ["github.com/Toricane", "github"],
  ["github.com/kevinjosethomas", "github"],
  ["x.com", "xTwitter"],
  ["youtube.com", "youtube"],
  ["instagram.com", "instagram"],
  ["medium.com", "medium"],
];

function iconNameForLink(href = "", ariaLabel = "") {
  const haystack = `${href} ${ariaLabel}`.toLowerCase();
  for (const [needle, name] of HREF_ICON_RULES) {
    if (haystack.includes(needle.toLowerCase())) return name;
  }
  return null;
}

function setSvgIcon(host, name, doc) {
  if (!name) return;
  let span = host.querySelector(".svg-icon");
  if (!span) {
    span = doc.createElement("span");
    span.className = "svg-icon";
    host.insertBefore(span, host.firstChild);
  }
  span.innerHTML = iconHtml(name);
}

/** Refresh inline SVGs for links/buttons (runs each build). */
export function applySvgIcons(root) {
  const doc = root.ownerDocument || root;

  root.querySelectorAll(".connections a[href]").forEach((link) => {
    setSvgIcon(
      link,
      iconNameForLink(link.getAttribute("href"), link.getAttribute("aria-label")),
      doc,
    );
  });

  const themeToggle = root.getElementById?.("theme-toggle") ||
    root.querySelector?.("#theme-toggle");
  if (themeToggle) {
    const theme =
      root.documentElement?.getAttribute("data-theme") ||
      root.querySelector?.("html")?.getAttribute("data-theme") ||
      "dark";
    setSvgIcon(themeToggle, theme === "light" ? "sun" : "moon", doc);
  }

  root.querySelectorAll(".scroll-more").forEach((btn) => {
    setSvgIcon(btn, "chevronDown", doc);
  });

  root.querySelectorAll(".timeline-toggle").forEach((btn) => {
    setSvgIcon(btn, "chevronDown", doc);
  });
}
